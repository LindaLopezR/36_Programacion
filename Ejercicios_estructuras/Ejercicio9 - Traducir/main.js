var palabra = prompt ('Introduce una palabra');
switch (palabra) {
	case 'casa': console.log ('house');
	break;
	case 'mesa': console.log ('table');
	break;
	case 'perro': console.log ('dog');
	break;
	case 'gato': console.log ('cat');
	break;
	default: console.log ('No hay traducción');
}